package FileSerach;

public class Result
{

    private boolean isFound;

    private String path;

    public void setIsFound(boolean isFound)
    {
        this.isFound = isFound;
    }

    public String getPath()
    {
        return path;
    }

    public void setPath(String path)
    {
        this.path = path;
    }

    public boolean getIsFound()
    {
        return isFound;
    }
}
